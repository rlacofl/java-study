package lang.string;

public class CharArrayMain {

    public static void main(String[] args) {

        char a = 'a';
        System.out.println(a);

        // char 를 사용해서 여러 문자를 나열하려면 char[] 사용해야함
        char[] charArr = new char[] {'h', 'e', 'l', 'l', 'o'};
        System.out.println(charArr);

        String str = "hello";
        System.out.println(str);

    }
}
